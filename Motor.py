class Motor:
    def __init__(self):
        self.numero = 0
        self.potencia = ""

    def get_numero(self):
        return self.numero

    def set_numero(self, numero):
        self.numero = numero

    def get_potencia(self):
        return self.potencia

    def set_potencia(self, potencia):
        self.potencia = potencia
